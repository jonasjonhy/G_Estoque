    void showListAllProducts (){
       
        try {
            
            ((DefaultTableModel) tabela.getModel()).setNumRows(0);
            tabela.updateUI();
            
            String descr, quant, preco;
            ProdutoDAO dao = new ProdutoDAO();
            ResultSet rs = dao.BuscarTodosProdutos();
            
            while(rs.next()){      
                descr = rs.getString("descricao");
                quant = rs.getString("quantidade");
                preco = rs.getString("preco");
                DefaultTableModel stqProdutos = (DefaultTableModel) tabela.getModel();
                stqProdutos.addRow(new Object[]{descr,quant,preco});
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Erro:" + ex);
        }   
        
               
    }
    
